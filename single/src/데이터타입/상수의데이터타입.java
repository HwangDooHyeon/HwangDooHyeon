package 데이터타입;

public class 상수의데이터타입 {
    public static void main(String[] args) {

        // 상수 : 변하지 않는 값 (constant)

        int i = 1;
        // a는 변할 수 있는 값이기 때문에 변수
        // 1은 정해진, 변하지 않는 값이기 때문에 상수

        float f = 2.2F;
        double d = 2.2;

        byte b = 100;
        short s = 200;
        // byte와 short 뒤 정수는 int형이지만 자바에서는 byte와 short에 int형을 사용할 수 있게 허용 해줌
        long l = 2147483648L;

    }
}
